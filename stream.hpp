///////////////////////////////////////////////////////////////////////////////////////////////////
#ifndef STREAM_HPP
#define STREAM_HPP

///////////////////////////////////////////////////////////////////////////////////////////////////
#include "convert.hpp"

#include <string>
#include <utility>

///////////////////////////////////////////////////////////////////////////////////////////////////
///
/// \brief operator<<
///
/// stream-like operator<< for std::string with conversion
///
template<typename T>
std::string& operator<<(std::string& stream, const T& value)
{
    stream += convert::to(value);
    return stream;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
///
/// \brief operator<<
///
/// stream-like operator<< for std::string with conversion (rvalue version)
///
template<typename T>
std::string&& operator<<(std::string&& stream, const T& value)
{
    stream += convert::to(value);
    return std::move(stream);
}

///////////////////////////////////////////////////////////////////////////////////////////////////
#endif // STREAM_HPP
