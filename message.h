#include "except.h"
#include <string>
#include <syslog.h>

///////////////////////////////////////////////////////////////////////////////////////////////////
enum class level
{
    emerg   = LOG_EMERG,
    alert   = LOG_ALERT,
    critical= LOG_CRIT,
    error   = LOG_ERR,
    warning = LOG_WARNING,
    notice  = LOG_NOTICE,
    info    = LOG_INFO,
    debug   = LOG_DEBUG
};

void show_message(const std::string&, level= level::info);

void show_error(const std::string&);
void show_error(const except::exception&);

